					{ required: true, message: '所属小区不能为空', trigger: 'blur' },
				],
				beizhushuoming: [
					{ required: true, message: '备注说明不能为空', trigger: 'blur' },
				],
			}
		};
	},
	props: ["parent"],
	computed: {



	},
    components: {
    },
