	},
	methods: {
		
		// 下载
		download(file){
			window.open(`${file}`)
		},
		// 初始化
		init(id,type) {
			if (id) {
				this.id = id;
				this.type = type;
			}
			if(this.type=='info'||this.type=='else'){
				this.info(id);
			}else if(this.type=='logistics'){
				this.logistics=false;
				this.info(id);
			}else if(this.type=='cross'){
				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='cheweiquhao'){
							this.ruleForm.cheweiquhao = obj[o];
							this.ro.cheweiquhao = true;
							continue;
						}
						if(o=='cheweibianhao'){
							this.ruleForm.cheweibianhao = obj[o];
							this.ro.cheweibianhao = true;
							continue;
						}
						if(o=='leixing'){
							this.ruleForm.leixing = obj[o];
							this.ro.leixing = true;
							continue;
						}
						if(o=='yonghuming'){
							this.ruleForm.yonghuming = obj[o];
							this.ro.yonghuming = true;
							continue;
						}
						if(o=='yezhuxingming'){
							this.ruleForm.yezhuxingming = obj[o];
							this.ro.yezhuxingming = true;
							continue;
						}
						if(o=='cheweifei'){
							this.ruleForm.cheweifei = obj[o];
							this.ro.cheweifei = true;
							continue;
						}
						if(o=='suoshuxiaoqu'){
							this.ruleForm.suoshuxiaoqu = obj[o];
							this.ro.suoshuxiaoqu = true;
							continue;
